// --- Parse verbs.csv and extract verb infinitives ---
function parseCSV(line) {
  const parts = [];
  let current = "";
  let inQuote = false;
  for (const ch of line) {
    if (ch === '"') { inQuote = !inQuote; continue; }
    if (ch === "," && !inQuote) { parts.push(current); current = ""; continue; }
    current += ch;
  }
  parts.push(current);
  return parts;
}

const csv = fs.readFileSync(csvPath, "utf8");
const lines = csv.split("\n").slice(1).filter((l) => l.trim());

// Add all verb infinitives to glossary
const glossary = { ...baseDictionary };
let verbsAdded = 0;

lines.forEach((line) => {
  const parts = parseCSV(line);
  const russianVerb = (parts[1] || "").trim().toLowerCase();
  const englishVerb = (parts[3] || "").trim();
  if (russianVerb && englishVerb && !glossary[russianVerb]) {
    glossary[russianVerb] = englishVerb;
    verbsAdded++;
  }
});

// Sort by key for readability
const sorted = {};
Object.keys(glossary)
  .sort()
  .forEach((k) => {
    sorted[k] = glossary[k];
  });

fs.writeFileSync(outPath, JSON.stringify(sorted, null, 2), "utf8");

// Stats
const totalEntries = Object.keys(sorted).length;

// Check coverage against actual sentences
let totalTokens = 0;
let matchedTokens = 0;
const unmatchedWords = {};

lines.forEach((line) => {
  const parts = parseCSV(line);
  const ruSentence = parts[4] || "";
  const words = ruSentence
    .replace(/[^а-яёА-ЯЁa-zA-Z\s-]/g, "")
    .split(/\s+/)
    .filter(Boolean);
  words.forEach((w) => {
    const lower = w.toLowerCase();
    totalTokens++;
    if (glossary[lower]) {
      matchedTokens++;
    } else {
      unmatchedWords[lower] = (unmatchedWords[lower] || 0) + 1;
    }
  });
});

const coverage = ((matchedTokens / totalTokens) * 100).toFixed(1);
console.log(`Generated ${outPath}`);
console.log(`Total entries: ${totalEntries} (${verbsAdded} from verbs.csv)`);
console.log(`Token coverage: ${matchedTokens}/${totalTokens} = ${coverage}%`);

